<h2>X-Totals, Z-Totals</h2>
<p>Note: X-Totals do <b>not</b> reset card totals. Z-Totals <b>do</b> reset card totals.</p>
<ol>
    <li><code>≡</code> (top left) to open the sidebar.</li>
    <li>Tap 'Totals / Reports'.</li>
    <li>Enter your supervisor code with the numpad, tap some whitespace to hide the keyboard, and tap 'Confirm'.</li>
    <li>Tap 'X-Totals' or 'Z-Totals' on the sidebar.</li>
    <li>Tap 'Yes'.</li>
</ol>
<p>You will likely need the topmost number on the right under the 'GRAND TOTAL' section*.</p>

<hr>

<h2>End of Day Banking</h2>
<ol>
    <li><code>≡</code> (top left) to open the sidebar.</li>
    <li>Tap 'Totals / Reports'.</li>
    <li>Enter your supervisor code with the numpad, tap some whitespace to hide the keyboard, and tap 'Confirm'.</li>
    <li>Tap 'End of Day Banking' on the sidebar.</li>
    <li>Tap 'Yes'.</li>
    <li>Tap 'All' for all card types.</li>
</ol>
<p>You will likely not need any numbers from this section*.</p>

