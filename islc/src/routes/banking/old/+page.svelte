<h2>X-Totals, Z-Totals</h2>
<p>Note: X-Totals do <b>not</b> reset card totals. Z-Totals <b>do</b> reset card totals.</p>
<ol>
    <li><code>Menu</code> <code>Menu</code> to enter supervisor menu.</li>
    <li><code>Enter</code> to select 'Totals'.</li>
    <li><code>↓</code>/<code>↑</code> then <code>Enter</code> to select 'X-Totals' or 'Z-Totals'.</li>
    <li>Enter your supervisor code with the numpad and press <code>Enter</code>.</li>
</ol>
<p>You will likely need the topmost number on the right under the 'GRAND TOTAL' section*.</p>

<hr>

<h2>End-of-Day Banking</h2>
<ol>
    <li><code>Menu</code> <code>Menu</code> to enter supervisor menu.</li>
    <li><code>Enter</code> to select 'Totals'.</li>
    <li><code>↓</code>/<code>↑</code> then <code>Enter</code> to select 'End-of-Day Banking'.</li>
    <li>Enter your supervisor code with the numpad and press <code>Enter</code>.</li>
    <li><code>Enter</code> to select all card types.</li>
</ol>
<p>You will likely not need any numbers from this section*.</p>

